package Entities;

public class Person {
    String name;
    int  age;
    int  id;
    int contactNumber;
    String address;

    public Person(String name, int age, int id, int contactNumber, String address) {
        this.name = name;
        this.age = age;
        this.id = id;
        this.contactNumber = contactNumber;
        this.address = address;
    }
}
